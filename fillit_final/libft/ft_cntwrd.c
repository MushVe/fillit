int	ft_cntwrd(char const *s, char c)
{
	int n;
	int is_word;

	n = 0;
	if (*s++ != c)
		n++;
	while (*s)
	{
		is_word = 0;
		while (*s && *s == c)
			s++;
		while (*s && *s != c)
		{
			s++;
			is_word = 1;
		}
		n += is_word;
		if (!is_word && *s)
			s++;
	}
	return (n);
}
