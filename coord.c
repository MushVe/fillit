#include "fillit.h"

void	empty_line(char *str, i)
{
	int j;

	if (i % 5 != 0)
		return (0);
	j = 0;
	while (j < 5 && str[i + j] == '.')
		j++;
	if (j == 4)
		return (1);
	return (0);
}

